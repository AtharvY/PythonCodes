""""Sequential data types
1. List: []"""
l=["bag", "vegetables", 105.10, 100]
l1=["bag", "vegetables", 105.10, 100]
print(l is l1)
print(l == l1)
print(id(l))
print(id(l1))
l.insert(4, "laptop")
print(l)
print()


""""Sequential data types
2. Tuple:()"""
tu=("laptop", "Python", 3.8, 16)
tu1=("laptop", "Python", 3.8, 16)
print(tu is tu1)
print(tu==tu1)
print(id(tu))
print(id(tu1))


""""Sequential data types
3. Set: {}"""
se={10,20,30,40,50,60,10,20}
print(se)
print(id(se))


